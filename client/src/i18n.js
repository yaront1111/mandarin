// Initialize i18n
i18n
  .use(LanguageDetector)
  .use(initReactI18next)
  .init({
    resources: {
      en: {
        translation: englishTranslations
      },
      he: {
        translation: hebrewTranslations
      }
    },
    fallbackLng: 'en',
    debug: process.env.NODE_ENV === 'development',

    interpolation: {
      escapeValue: false, // React already escapes values
    },

    detection: {
      // Use localStorage to cache user language preference
      order: ['navigator', 'localStorage', 'querystring', 'cookie'],
      lookupQuerystring: 'lng',
      lookupCookie: 'i18next',
      lookupLocalStorage: 'i18nextLng',
      caches: ['localStorage'],
    },

    // Handling RTL for Hebrew
    react: {
      useSuspense: true,
      bindI18n: 'languageChanged',
      bindI18nStore: '',
      nsMode: 'default'
    }
  });

// Add a language change listener to directly set HTML language and direction attributes
i18n.on('languageChanged', (lng) => {
  console.log("Language changed to:", lng);
  
  // Set the HTML lang attribute
  document.documentElement.setAttribute('lang', lng);
  
  // Set the HTML dir attribute for RTL/LTR
  if (lng === 'he') {
    document.documentElement.setAttribute('dir', 'rtl');
    document.documentElement.classList.add('rtl');
    document.body.classList.add('rtl-layout');
    
    // Force layout recalculation
    document.body.offsetHeight;
    
    // Add RTL class to any existing modals or key containers
    document.querySelectorAll('.modal-container, .profile-content, .app-wrapper').forEach(el => {
      if (el) el.classList.add('rtl-layout');
    });
  } else {
    document.documentElement.setAttribute('dir', 'ltr');
    document.documentElement.classList.remove('rtl');
    document.body.classList.remove('rtl-layout');
    
    // Force layout recalculation
    document.body.offsetHeight;
    
    // Remove RTL class from any existing modals or key containers
    document.querySelectorAll('.modal-container, .profile-content, .app-wrapper').forEach(el => {
      if (el) el.classList.remove('rtl-layout');
    });
  }
  
  // Fire a custom event that components can listen for
  window.dispatchEvent(new CustomEvent('i18n:languageChanged', { 
    detail: { language: lng, isRTL: lng === 'he' } 
  }));
});

export default i18n;
